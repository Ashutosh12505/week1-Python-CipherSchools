p = "abcdefghij"
#syntax = [start:stop:step]

print(p[::2]) #prints acegi

print("zebra"[0:5:2]) #prints zba

#we can give step value as -1 to go backwards from the end.

a = "scisyhp"
print(a[6::-1])

print("dolphins"[-1::-1]) #reverses the string.
print("Himalayas"[::-1])  #this also reverses the string.
print("Meghalayas"[::])  #prints the same string.