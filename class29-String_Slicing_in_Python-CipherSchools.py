#slicing/slecting sub-sequences.

var_2 = "Indian Premier League"
#space between words also takes a place.

#syntax = [start argument:stop argument]

print(var_2[2:4])

print(var_2[2:]) #will print till end.
#or
print(var_2[2:21])

print(var_2[:4]) #from beginning till 3rd.
print(var_2[5:8]) #will include space in the result
print(var_2[-6:-2]) #Leag
print(var_2[:]) #complete string